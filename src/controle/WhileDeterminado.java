package controle;

public class WhileDeterminado {
	
	public static void main(String[] args) {
		
		int contador = 1;
		
		while(contador <= 20) {
		System.out.printf("i = %d\n", contador);  // Printf equlale as operações equivalentes ao Float
		contador ++ ;
		
		
		
		// While se repete intermitende toda vez enquanto for verdade, de quantidades inteterminadas de repetições
		
		
		}
		
	}
	

}
