package javaOperadoresDeAtribuiçãoAritméticosRelacionaisElógicos;


public class OperadoresdeIgualdade {
	
	public static void main(String[] args) {
		
		//Os operadores de igualdade verificam se o valor ou o resultado da expressão lógica 
		//à esquerda é igual (“==”) ou diferente (“!=”) ao da direita, retornando um valor booleano.
		
	

		
		int valorA = 1;
		int valorB = 2;

		if(valorA == valorB) {
		   System.out.println("Valores iguais");
		} else {
			System.out.println("Valores diferentes");
		}
		
		
	}

}
